    private void pid() {
        try {
            ArrayList<com.mangroven.hotel.dto.Food_PackageDTO> packageList = packCntrl.getAllID();
            for (int i = 0; i < packageList.size(); i++) {
                com.mangroven.hotel.dto.Food_PackageDTO reserve = (com.mangroven.hotel.dto.Food_PackageDTO) packageList.get(i);
                comboPack.addItem(reserve.getPID());

            }
        } catch (Exception ex) {
            Logger.getLogger(modPackage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void searchPackage() {
        String pid = (String) comboPack.getSelectedItem();
        DefaultTableModel dtm = (DefaultTableModel) table.getModel();
        dtm.setRowCount(0);
        ArrayList<FoodDTO> food = query.getPackageDetail(pid);
        for (FoodDTO r1 : food) {
            Object[] row = {r1.getFID(), r1.getFood_Name()};
            dtm.addRow(row);
        }
    }

    private void delete() {
        try {
            String pack = (String) comboPack.getSelectedItem();
            boolean delete = packCntrl.delete(pack);
            if (delete) {
                JOptionPane.showMessageDialog(null, " Delete Success");
                clear();
            } else {
                JOptionPane.showMessageDialog(null, " Delete Success");
            }
        } catch (Exception ex) {
            Logger.getLogger(modPackage.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

