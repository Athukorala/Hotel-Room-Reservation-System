    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        if (txtName.getText().equals("") | txtPrice.getText().equals("") | table.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Sorry! Incomplete...", "Build Package", JOptionPane.WARNING_MESSAGE);

        } else {

            try {
                String pid = (String) comboPack.getSelectedItem();
                String name = txtName.getText();
                double price = Double.parseDouble(txtPrice.getText());

                DefaultTableModel dtm = (DefaultTableModel) table.getModel();
                ArrayList<Food_Package_DetailDTO> detail = new ArrayList<>();
                for (int i = 0; i < dtm.getRowCount(); i++) {
                    String fid = (String) dtm.getValueAt(i, 0);
                    Food_Package_DetailDTO fdDto = new Food_Package_DetailDTO(pid, fid);
                    detail.add(fdDto);
                }
                Food_PackageDTO foodPackge = new Food_PackageDTO(pid, name, price, detail);
                System.out.println(pid + " " + name + " " + price + " " + detail);

                boolean isAdd = packCntrl.transactionPackageUpdate(foodPackge);

                if (isAdd) {
                    JOptionPane.showMessageDialog(null, "Added Success");
                    clear();

                } else {
                    JOptionPane.showMessageDialog(null, "Addedfjdkfjkd Failed");
                }
            } catch (Exception ex) {
                Logger.getLogger(skipPanelAssemble.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnAddMouseClicked

