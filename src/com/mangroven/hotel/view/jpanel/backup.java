    private void view() throws Exception {
        try {
            ArrayList<CostDTO> cost = costCntrl.getAllName();
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            for (CostDTO c1 : cost) {
                Object[] rowData = {c1.getCostID(), c1.getDescription(), c1.getCost(), c1.getDate(), c1.getTime()};
                dtm.addRow(rowData);
            }
        } catch (SQLException e) {
            System.out.println(e);
        } catch (ClassNotFoundException e) {
            System.out.println(e);

        }

    }

