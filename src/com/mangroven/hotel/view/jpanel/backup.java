    private void getProfit() {

        PaymentDTO dto = payCntrl.getProfit();
        if (dto.getNewAmount() <= 0) {
            lblProfit.setText(" Cost      " + ": " + "  ( " + dto.getAmount() + " )  " + " - " + "  ( " + dto.getAdvance() + " )" + "   :   " + "( " + dto.getNewAmount() + " )");
            lblProfit.setForeground(Color.red);
            lblIcon.setIcon(new javax.swing.ImageIcon(getClass().getResource("/hotel/icon/sad.png")));
        } else {
            lblProfit.setText(" Profit      " + ": " + "  ( " + dto.getAmount() + " )  " + " - " + "  ( " + dto.getAdvance() + " )" + "   :   " + "( " + dto.getNewAmount() + " )");
            lblProfit.setForeground(new Color(21, 190, 41));
            lblIcon.setIcon(new javax.swing.ImageIcon(getClass().getResource("/hotel/icon/happy.png")));
        }
    }
