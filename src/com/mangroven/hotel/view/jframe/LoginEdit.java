    private void verifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verifyActionPerformed
        try {
            PasswordDTO p1 = psw.getAllDetail();

            if (newPassword.getText().equals(verify.getText()) && password.getText().equals(p1.getPassword())) {
                lblVeri.setIcon(new javax.swing.ImageIcon(getClass().getResource("/hotel/icon/AA.png")));
                lblNew.setIcon(new javax.swing.ImageIcon(getClass().getResource("/hotel/icon/AA.png")));
                add();

            } else {

                lblVeri.setIcon(new javax.swing.ImageIcon(getClass().getResource("/hotel/icon/BB.png")));
                JOptionPane.showMessageDialog(LoginEdit.this, "Incomplete");
                verify.requestFocus();
            }
        } catch (Exception ex) {
            Logger.getLogger(LoginEdit.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_verifyActionPerformed

