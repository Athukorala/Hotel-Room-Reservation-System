    private void add() {
        try {
            String id = lblId.getText();
            String user = name.getText();
            String Verify = verify.getText();
           
            PasswordDTO p1 = new PasswordDTO(id, user, Verify);
            boolean update = psw.update(p1);
            if (update) {
                JOptionPane.showMessageDialog(LoginEdit.this, "Ok! Password Changed");
                name.setText("");
                password.setText("");
                newPassword.setText("");
                verify.setText("");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(LoginEdit.this, "Sorry TryAgain");
                name.setText("");
                password.setText("");
                newPassword.setText("");
                verify.setText("");
                name.requestFocus();
                icon();
            }
        } catch (Exception ex) {
            Logger.getLogger(loginNew.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

