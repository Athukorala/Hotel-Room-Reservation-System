    private void passwordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_passwordActionPerformed
        try {

            PasswordDTO p1 = psw.getAllDetail();

            if (password.getText().equals(p1.getPassword())) {
                lblOld.setIcon(new javax.swing.ImageIcon(getClass().getResource("/hotel/icon/AA.png")));
                newPassword.setEnabled(true);
                newPassword.requestFocus();

            } else {
                lblOld.setIcon(new javax.swing.ImageIcon(getClass().getResource("/hotel/icon/BB.png")));
                password.requestFocus();

                password.setText("");
            }
        } catch (Exception ex) {
            Logger.getLogger(loginNew.class.getName()).log(Level.SEVERE, null, ex);
        }// TODO add your handling code here:
    }//GEN-LAST:event_passwordActionPerformed

